import UIKit

extension UIColor {
    
    static func twPurple(alpha: CGFloat = 1.0) -> UIColor {
        return UIColor(red: 134/255, green: 36/255, blue: 126/255, alpha: alpha)
    }
    
    static func twOrange(alpha: CGFloat = 1.0) -> UIColor {
        return UIColor(red: 206/255, green: 95/255, blue: 55/255, alpha: alpha)
    }
    
}